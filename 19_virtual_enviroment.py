### virtual environment is a way to create a isolated environment for a project

## Create a virtual environment
# python -m venv myenv

## Activate the virtual environment ( Linux/Mac)
# source myenv/bin/activate

## Activate the virtual environment ( Windows )
# myenv\Scripts\activate.bat

## Deactivate the virtual environment
# deactivate

## The "requirements.txt" file is used to install the dependencies for a project

## To create the "requirements.txt" file
# pip freeze > requirements.txt

## To install the dependencies from the "requirements.txt" file
# pip install -r requirements.txt

